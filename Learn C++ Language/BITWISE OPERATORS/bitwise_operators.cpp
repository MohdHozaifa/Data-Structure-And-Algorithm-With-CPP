#include<iostream>
//#include<bits/stdc++.h>
using namespace std;

int main()
{
    int a = 4;
    int b = 6;

    cout<<" a&b " << (a&b) <<endl;    // AND
    cout<<" a|b " << (a|b) <<endl;    // OR
    cout<<" ~a " << (~a) <<endl;      // NOT
    cout<<" ~b " << (~b) <<endl;
    cout<<" a^b " << (a^b) <<endl;    // XOR
    cout<< (a>>1);
}


